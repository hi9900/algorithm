n = input()

# 좌표는 8 x 8 (1~8, a~h)

l = ["a", "b", "c", "d", "e", "f", "g", "h"]
c = ["1", "2", "3", "4", "5", "6", "7", "8"]

# 나이트의 좌표
ni = c.index(n[1])
nj = l.index(n[0])

# 이동할 수 있는 경우
dxy = [(-2, 1), (-2, -1), (2, 1), (2, -1),
       (1, -2), (-1, -2), (1, 2), (-1, 2)]

cnt = 0
for dx, dy in dxy:
    nx = ni + dx
    ny = nj + dy

    # 좌표를 벗어나지 않으면,
    if 0 <= nx <= 7 and 0 <= ny <= 7:
        cnt += 1

print(cnt)
